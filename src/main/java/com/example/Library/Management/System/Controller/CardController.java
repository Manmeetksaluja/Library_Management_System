package com.example.Library.Management.System.Controller;

import com.example.Library.Management.System.Entities.Librarycard;
import com.example.Library.Management.System.Service.CardService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.*;

@RestController
@RequestMapping("/card")
public class CardController {
    @Autowired//object is created
    private CardService cardservice;
   @PostMapping("/generateplanecards")//used for ading to databass
    public ResponseEntity generateplanecards(){
          //cardno=autogenerated
        //cardstatus=new
        //card is associated with whom will be generated by another api
        //plane plastic cards will be generated without any student associated with it
        Librarycard newcard=cardservice.generatedcard();//in newcard generatecard function of service layer of card will generate a new card
        String response = "The new card is generated having the cardnumber" + newcard.getCardno();
        return new ResponseEntity(response , HttpStatus.OK);
    }
    @PutMapping("/associatewithstudent")
    public ResponseEntity associatewithstudent(@RequestParam ("studentId") Integer studentId , @RequestParam ("cardno") Integer cardno ){
               String result=cardservice.associatewithtudent(studentId , cardno);
               return new ResponseEntity(result , HttpStatus.OK);
    }
}
